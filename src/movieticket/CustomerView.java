    public final CustomerModel getData()
    {
        try {
            arr=new ArrayList<>();
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/movie", "root", "");
            Statement st=con.createStatement();
            //To change
            ResultSet rs=st.executeQuery("select movie_name,hall_no,date_format(date,'%d-%m-%y') as date,time,seats,no_of_seats,price,poster from details,payment where s_no= mov_no and mail="+mail+" order by date desc");            
            int s_no=0;
            while(rs.next())
            {
                s_no++;
                arr.add(new Cus(s_no, rs.getInt("no_of_seats"), rs.getInt("price"), rs.getInt("hall_no"), rs.getString("movie_name"), rs.getString("seats"), rs.getString("date"), rs.getString("time"), rs.getBytes("poster")));
            }
            String[] columns={"S. NO","Movie Name","Hall No","Date","Time","Seats","NO of Seats","Price","Poster"};
            Object[][] rows=new Object[arr.size()][10];
            for (int i = 0; i < arr.size(); i++) {
                rows[i][0]=arr.get(i).getS_no();
                rows[i][1]=arr.get(i).getMovName();
                rows[i][2]=arr.get(i).getHallno();
                rows[i][3]=arr.get(i).getDate();
                rows[i][4]=arr.get(i).getTime();
                rows[i][5]=arr.get(i).getSeats();
                rows[i][6]=arr.get(i).getNoofseats();
                rows[i][7]=arr.get(i).getPrice();
                if (arr.get(i).getPoster()!= null) {
                    ImageIcon image = new ImageIcon(new ImageIcon(arr.get(i).getPoster()).getImage()
                            .getScaledInstance(150, 120, Image.SCALE_SMOOTH));
                    rows[i][8] = image;
                } else {
                    rows[i][8] = null;
                }
            }
            CustomerModel mod=new CustomerModel(rows,columns);
            jTable1.setRowHeight(120);
            return mod;
        } catch (SQLException ex) {
            System.out.println(ex.toString());
            JOptionPane.showMessageDialog(null, "Check database connection");
        }
        return null;
    }

