    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if(jTable1.getSelectedRowCount()==1)
        {
            try {
                int row=jTable1.getSelectedRow();
                String date=arr.get(row).getDate();
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/movie","root","");
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery("select datediff(date(now()),date_format(\'"+date+"\',\'%d-%m-%y\')) from dual");
                rs.next();
                int diff=rs.getInt(1);
                System.out.println(diff);
                if(diff<0)
                {
                    cancel.setVisible(true);
                }
                else if(diff==0)
                {
                    String time=arr.get(row).getTime();
                    rs=st.executeQuery("select timediff(time(now()),\'"+time+":00\') from dual");
                    rs.next();
                    String[] timediff=rs.getString(1).split(":");
                    if(Integer.parseInt(timediff[0])>=6)
                    {
                        cancel.setVisible(true);
                    }
                    
                }
                else
                {
                    cancel.setVisible(false);
                }
            } catch (SQLException ex) {
                cancel.setVisible(false);
                Logger.getLogger(CustomerView.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

